package academy.devdojo.javaoneforall.introduction;

public class Class02PrimitivesTypes {
    public static void main(String[] args){
        // int, double, float, boolean, char, byte, short, long
        int age = 10;
        long bigNumber = 10000;
        float salaryFloat = 2500.5F;
        double salaryDouble = 5000.0;
        boolean trueValue = true;
        boolean falseValue = false;
        short ageShort = 29;
        byte ageByte = 39;
        char character = 'w';
        char number = 56;
        String name  = "Goku";
        //string is not a primitive type, so the fist letter is upper case
        //and the string stay between double quotes
        System.out.println("the age is = " + age);
        System.out.println("HI, my name is " +name);
    }
}
